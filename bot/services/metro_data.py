def get_metro_cities() -> list:
    """Получить список городов с метро"""
    return list(METRO_CITIES.keys())


def get_metro_stations(city: str) -> list:
    """Получить станции метро для города"""
    return METRO_CITIES.get(city, {}).get('stations', [])


def get_random_stations(city: str, count: int) -> list:
    """Получить случайные станции метро для города"""
    stations = get_metro_stations(city)
    if not stations:
        return []

    # Если нужно больше станций чем есть, повторяем некоторые
    if count > len(stations):
        import random
        result = stations.copy()
        while len(result) < count:
            result.append(random.choice(stations))
        return result[:count]
    else:
        import random
        return random.sample(stations, count)


def generate_metro_address(city: str, metro_station: str, ad_number: int = 1) -> str:
    """Генерация адреса рядом со станцией метро"""
    import random

    # Типы улиц рядом с метро
    street_types = [
        f"ул. у метро {metro_station}",
        f"пр. возле {metro_station}",
        f"пер. у станции {metro_station}",
        f"б-р рядом с {metro_station}",
        f"пл. у метро {metro_station}",
        f"наб. около {metro_station}"
    ]

    street = random.choice(street_types)
    building = random.randint(1, 50)

    if ad_number > 1:
        # Для нескольких объявлений добавляем квартиры
        apartment = ad_number
        return f"{city}, {street}, д. {building}, кв. {apartment}"
    else:
        return f"{city}, {street}, д. {building}"